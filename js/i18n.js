// ─── State ───────────────────────────────────────────────
let _lang = 'pt';

// ─── Public API ─────────────────────────────────────────
function t(key) {
  return TRANSLATIONS[_lang]?.[key] ?? TRANSLATIONS.pt[key] ?? key;
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  _lang = lang;
  window._lang = lang;
  localStorage.setItem('erb_lang', lang);
  applyI18n();
  renderLangSwitcher();
  if (typeof updateSEO === 'function') updateSEO(window._currentPage || 'search');
}

function getLang() { return _lang; }

// ─── DOM application ────────────────────────────────────
function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== key) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.getAttribute('data-i18n-placeholder'));
    if (val) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const val = t(el.getAttribute('data-i18n-title'));
    if (val) el.title = val;
  });
  document.documentElement.lang = LANGS[_lang]?.locale || 'pt-BR';
}

// ─── Language switcher UI ────────────────────────────────
function renderLangSwitcher() {
  const html = Object.entries(LANGS).map(([code, info]) =>
    `<button class="lang-btn${_lang === code ? ' on' : ''}"
             onclick="setLang('${code}')"
             title="${info.name}"
             aria-label="${info.name}">
       ${info.flag}
     </button>`
  ).join('');
  // Renderiza em todos os containers (.lang-switcher) — header do app E
  // header da landing page têm o mesmo seletor.
  document.querySelectorAll('.lang-switcher').forEach(el => { el.innerHTML = html; });
}

// ─── Init ────────────────────────────────────────────────
function initI18n() {
  const stored  = localStorage.getItem('erb_lang');
  const browser = (navigator.language || 'pt').split('-')[0];
  const supported = Object.keys(TRANSLATIONS);
  // Map common browser codes
  const browserMap = { 'pt': 'pt', 'en': 'en', 'es': 'es', 'ja': 'ja', 'de': 'de', 'fr': 'fr' };
  _lang = supported.includes(stored)            ? stored
        : supported.includes(browserMap[browser]) ? browserMap[browser]
        : 'pt';
  window._lang = _lang;
  renderLangSwitcher();
  applyI18n();
}
